   <p><strong>1. Opt For Automatic Software Updates</strong></p>
   <p>To help protect their devices from cybercrime, consumers should make sure to turn on automatic updates for all
       software, especially for frequently used browsers and apps. Many people forget to update manually. Hackers target
       weaknesses in programs that can later be fixed with a patch or update—but only if the consumer keeps their software
       current. This reduces new threats.</p>
   <p><strong>2. Stay Skeptical</strong></p>
   <p>Maintain a healthy degree of skepticism about everything you receive from digital sources, whether it’s via email, a
       text message, chat messages or even a good old-fashioned telephone call. If it sounds too good to be true, it
       probably is. If it comes with an urgent call to action, why? Much of cybercrime today leverages these forms of
       phishing attacks, so awareness is at least as important as technology. -</p>
   
   <p><strong>3. Never Reuse Passwords</strong></p>
   <p>Use unique passwords for every website. Otherwise, if one website is compromised, the same credentials could be used
       to authenticate to other services. Of course, no one would be able to remember all these passwords without a
       password manager. Luckily there are many services out there that are all better than using the same password all
       over the internet.</p>
   
   <p><strong>4. Use A Password Manager</strong></p>
   <p>Passwords are the critical piece because consumers end up reusing them. This practice can be easily avoided by using
       a password manager. There are several good open-source options out there that are safe. In addition to generating
       strong passwords, they also enable you to use two-factor authentication, which makes hacking an account much harder.
      </p>
   <p><strong>5. Leverage Google Authenticator</strong></p>
   <p>A great place to start is to think about credentials. A couple of tools I would recommend everyone use daily are
       Google Authenticator, which allows you to have multifactor and adaptive authentication to commonly used services
       such as Gmail and Instagram, and password vaulting programs, which are critical when looking at the gravity of
       credential-based attacks. </p>
   <p><strong>6. Always Use A VPN</strong></p>
   <p>My suggestion is simple: Always use a VPN. There are many affordable solutions for everyday consumers, and this one
       small technical precaution goes a long way toward making your communication more secure. </p>
   <p><strong>7. Confirm All Communications</strong></p>
   <p>Do not send out your private information, and always confirm the identity of anyone you are chatting with or
       emailing. In a way, human communication can become your own multifactor authentication. </p>
   <p><strong>8. Adopt A ‘Zero-Trust’ Rule</strong></p>
   <p>I would suggest consumers adopt the “zero-trust” rule—don’t trust anyone with your personal information and data.
       Internet and technology end users must not rely on their service or product providers or governments to protect them
       from cybercrimes. Rather, technology consumers must take the initiative by learning at least the basic safety
       procedures to protect themselves. </p>
   <p><strong>9. Stay Away From Links And Attachments</strong></p>
   <p>The majority of successful cybercrimes begin when curiosity or a sense of urgency entices an average consumer to
       enter personal data or click on a link that downloads malware. Besides having a security suite on your devices, the
       simplest way to avoid being a target is to stay away from links and attachments that go directly to spoofed
       websites, ask for your data or request passwords. </p>
   <p><strong>10. Check Online Breach Reports</strong></p>
   <p>At this point, assume the worst. It’s not if you’ll be hacked, but when. Change your passwords frequently and invest
       in a two-factor authentication solution. A handy resource to periodically visit is <a
           href="https://haveibeenpwned.com/" target="_blank" class="color-link" title="https://haveibeenpwned.com/"
           rel="nofollow noopener noreferrer" data-ga-track="ExternalLink:https://haveibeenpwned.com/"
           aria-label="haveibeenpwned.com">haveibeenpwned.com</a>, which routinely tracks the largest security breaches and
       lets you quickly check whether your information has already been compromised. </p>
   <p><strong>11. Don’t Share Personal Information Online</strong></p>
   <p>Stop giving out so much information online. Do not do online quizzes or use add-on apps for social media unless you
       know the originating company is safe and won’t use the data you give them against you. I see a lot of people
       voluntarily giving security question answers on social media. They think it’s just fun sharing for friends, not
       realizing they are giving hackers the keys to their accounts. </p>
   <p><strong>12. Never Trust The Default Settings</strong></p>
   <p>Whenever you’re opening a new account or downloading a new app, the first thing you should do is have a close look at
       the security settings. Options such as multifactor or two-factor authentication and other safeguards such as
       biometrics may or may not be enabled. Conduct a similar audit on any current apps, platforms or services you’re
       using. </p>
   <p><strong>13. Don’t Connect To The Internet If You Don’t Have To</strong></p>
   <p>If a device does not need an internet connection to work, then don’t connect it. Every connected device is an avenue
       for attack. Reduce the opportunities for attack by disconnecting devices that perform simple tasks such as keeping
       food cold or washing clothes. The internet is not required to keep lettuce fresh. </p>
   <p><strong>14. Take A Three-Step Approach</strong></p>
   <p>Cybercrime is an increasingly real and present danger for organizations and consumers alike. An easy three-tiered
       protocol consumers can use to protect themselves is to install antivirus software on their computers, change their
       passwords frequently and never give out personally identifiable information. Following these policies can greatly
       reduce your chances of becoming a victim of cybertheft. 
   
   </p>
   <p><strong>15. Educate Yourself</strong></p>
   <p>My top tip is to get training. Education is power. Read blogs and/or sign up for online courses around personal
       security. A good course will cover many critical aspects. Among the words and phrases to search for are “identifying
       phishing,” “checking SSL certs,” “strong passwords,” “password managers,” “malware scanning,” “system updates,”
       “malicious files,” “antivirus” and “security settings.” 
           </p>
   
